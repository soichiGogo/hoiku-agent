def _grid(
    seimei: str, jocho: str, kenko: str, ningen: str, kankyo: str, kotoba: str, hyogen: str
) -> list[dict]:
    """クラス月案の区分×領域グリッド（養護2本柱＋教育5領域）。ねらい（aim）を領域別に渡す。

    区分（category）は schemas 側の model_validator が領域から決定的に補完する（正準7行・§18）。
    """
    rows = [
        ("生命の保持", seimei),
        ("情緒の安定", jocho),
        ("健康", kenko),
        ("人間関係", ningen),
        ("環境", kankyo),
        ("言葉", kotoba),
        ("表現", hyogen),
    ]
    return [{"domain": d, "aim": a} for d, a in rows]


