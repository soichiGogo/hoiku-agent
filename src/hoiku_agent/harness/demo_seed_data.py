def child_of(kind: str, entry: dict) -> str:
    """既存判定用：書類の主対象児表示名（日誌・クラス月案はクラス単位＝空）。record_store と同じ規約。"""
    if kind in ("diary", "class_monthly"):
        return ""
    return str(entry.get("child_id") or "")


def target_of(kind: str, entry: dict) -> str:
    """既存判定用：対象期間キー（日誌=記録日・月案=対象月・保育経過記録=期間・要録=年度）。"""
    if kind == "diary":
        return str(entry.get("date") or "")
    if kind == "class_monthly":
        return str(entry.get("month") or "")
    if kind == "child_record":
        return str(entry.get("period") or "")
    return str(entry.get("fiscal_year") or "")


def entry_label(kind: str, entry: dict) -> str:
    """ログ用の書類ラベル（種別＋対象児/クラス＋対象期間）。"""
    if kind == "diary":
        return f"{KIND_LABEL[kind]} {entry['date']}（{entry.get('class_name', '')}）"
    if kind == "class_monthly":
        return f"{KIND_LABEL[kind]} {entry.get('class_name', '')} {entry.get('month', '')}"
    key = entry.get("month") or entry.get("period") or entry.get("fiscal_year") or ""
    return f"{KIND_LABEL[kind]} {entry.get('child_id', '')} {key}"


