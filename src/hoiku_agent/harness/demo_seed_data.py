def age_months_on(child_id: str, on: date) -> str:
    """生年月日から「N歳Mか月」を機械計算する（手書きしない＝月齢の整合を構造で保証・§14）。"""
    birth = BIRTHDATES[child_id]
    months = (on.year - birth.year) * 12 + (on.month - birth.month)
    if on.day < birth.day:
        months -= 1
    return f"{months // 12}歳{months % 12}か月"


