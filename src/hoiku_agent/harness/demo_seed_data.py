def _att(present: list[str], absent: list[tuple[str, str]] | None = None) -> list[dict]:
    """出欠（クラス日誌）。present=出席の仮名、absent=(仮名, 欠席理由)。"""
    rows = [{"child_id": c, "present": True, "reason": None} for c in present]
    rows += [{"child_id": c, "present": False, "reason": r} for c, r in (absent or [])]
    return rows


def _note35(child_id: str, age_months: str, observed_state: str, tags: list[str]) -> dict:
    """3–5 の個別記録（生活記録は標準様式に児別欄が無いため付けない＝§10/§19）。"""
    return {
        "child_id": child_id,
        "age_months": age_months,
        "observed_state": observed_state,
        "tags": tags,
    }


def _note02(
    child_id: str,
    age_months: str,
    observed_state: str,
    tags: list[str],
    *,
    meal: str,
    sleep: str,
    toilet: str,
    mood_health: str,
) -> dict:
    """0–2 の個別記録（養護の中核＝生活記録 食事/睡眠/排泄/機嫌・体調 を必ず添える＝§10）。"""
    return {
        "child_id": child_id,
        "age_months": age_months,
        "observed_state": observed_state,
        "tags": tags,
        "life_record": {
            "meal": meal,
            "sleep": sleep,
            "toilet": toilet,
            "mood_health": mood_health,
        },
    }


def _diary(
    date: str,
    age_band: str,
    *,
    class_name: str,
    weather: str,
    temperature: str,
    daily_aim: str,
    attendance: list[dict],
    practice_record: str,
    individual_notes: list[dict],
    child_focus: str,
    self_review: str,
) -> dict:
    return {
        "date": date,
        "age_band": age_band,
        "class_name": class_name,
        "weather": weather,
        "temperature": temperature,
        "daily_aim": daily_aim,
        "attendance": attendance,
        "practice_record": practice_record,
        "individual_notes": individual_notes,
        "evaluation": {"child_focus": child_focus, "self_review": self_review},
    }


def _grid(
    seimei: str, jocho: str, kenko: str, ningen: str, kankyo: str, kotoba: str, hyogen: str
) -> list[dict]:
    """クラス月案の区分×領域グリッド（養護2本柱＋教育5領域）。ねらい（aim）を領域別に渡す。

    区分（category）は schemas 側の model_validator が領域から決定的に補完する（正準7行・§18）。
    """
    rows = [
        ("生命の保持", seimei),
        ("情緒の安定", jocho),
        ("健康", kenko),
        ("人間関係", ningen),
        ("環境", kankyo),
        ("言葉", kotoba),
        ("表現", hyogen),
    ]
    return [{"domain": d, "aim": a} for d, a in rows]


def _igoal(child_id: str, age_months: str, child_state: str, aim_support: str) -> dict:
    """クラス月案（0–2）の個人目標小表の1行（子どもの姿・ねらい/配慮）。評価欄は月末記入＝AI 非生成。"""
    return {
        "child_id": child_id,
        "age_months": age_months,
        "child_state": child_state,
        "aim_support": aim_support,
    }


