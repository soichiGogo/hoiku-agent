# ════════════════════════════ 投入（finalize_entry で確定→save_document で永続化） ════════════════════════════

# 投入順は集積の依存に沿える（日誌→月案→クラス月案→保育経過記録→要録）。dedupe_key は種別×児×期間で一意
# （クラス月案は種別×月のみ＝年齢帯を含まないため月をずらして衝突回避・record_store._extract_child_display）。
_JOBS: list[tuple[str, list[dict]]] = [
    ("diary", DIARIES),
    ("monthly", MONTHLY_PLANS),
    ("class_monthly", CLASS_MONTHLY_PLANS),
    ("child_record", CHILD_RECORDS),
    ("nursery_record", NURSERY_RECORDS),
]

_KIND_LABEL = {
    "diary": "保育日誌",
    "monthly": "月案",
    "class_monthly": "クラス月案",
    "child_record": "保育経過記録",
    "nursery_record": "保育要録",
}


def _entry_label(kind: str, entry: dict) -> str:
    """ログ用の書類ラベル（種別＋対象児/クラス＋対象期間）。"""
    if kind == "diary":
        return f"{_KIND_LABEL[kind]} {entry['date']}（{entry.get('class_name', '')}）"
    if kind == "class_monthly":
        return f"{_KIND_LABEL[kind]} {entry.get('class_name', '')} {entry.get('month', '')}"
    key = entry.get("month") or entry.get("period") or entry.get("fiscal_year") or ""
    return f"{_KIND_LABEL[kind]} {entry.get('child_id', '')} {key}"


def _validate_all() -> list[str]:
    """全書類を finalize_entry に通して型成立を確認する（DB 不要・creds 不要）。違反の一覧を返す。"""
    from hoiku_agent.harness.finalize import finalize_entry

    failures: list[str] = []
    for kind, entries in _JOBS:
        for entry in entries:
            fd = finalize_entry(entry, kind=kind)
            label = _entry_label(kind, entry)
            if fd.parse_error:
                failures.append(f"[parse] {label}: {fd.parse_error}")
            for p in fd.problems:
                failures.append(f"[type] {label}: {p}")
    return failures


def main() -> None:
    parser = argparse.ArgumentParser(
        description="代表児の書類チェーン（日誌→月案→保育経過記録→要録）を書類アーカイブへ投入する seed"
    )
    parser.add_argument(
        "--dry-run",
        action="store_true",
        help="DB へ書かず、全書類の型成立（finalize_entry）だけ検査して結果を表示する",
    )
    parser.add_argument(
        "--approve",
        action=argparse.BooleanOptionalAction,
        default=True,
        help="保存後に承認済み（approved）にする（既定 True・--no-approve で確定止まり）",
    )
    parser.add_argument("--actor", default="seed", help="監査証跡の担当者名（自己申告・既定 seed）")
    args = parser.parse_args()

    total = sum(len(entries) for _, entries in _JOBS)
    print(
        f"書類 {total} 件（"
        + " / ".join(f"{_KIND_LABEL[k]}:{len(e)}" for k, e in _JOBS)
        + "）／対象児は実在しない仮名のみ（§14）"
    )

    # ── まず全書類の型成立を確認（ここが赤なら手書きデータのバグ＝投入前に必ず落とす） ──
    failures = _validate_all()
    if failures:
        print(f"\n型検査で {len(failures)} 件の違反（投入を中止します）:")
        for f in failures:
            print(f"  {f}")
        raise SystemExit(1)
    print("型検査: 全書類が型成立（必須欄・年齢分岐・様式整形 OK）")

    if args.dry_run:
        print("[dry-run] DB へは書き込んでいません。")
        return

    from hoiku_agent.harness import record_store
    from hoiku_agent.harness.finalize import finalize_entry

    status = record_store.store_status()
    if status != "ok":
        raise SystemExit(
            f"\n書類アーカイブへ接続できません（store_status={status}）。"
            "DATABASE_URL を設定し、マイグレーション適用済みの DB を指してください"
            "（手順は docs/ライブ実行手順.md）。型検査は上で通過しています。"
        )

    now = datetime.now()
    saved = approved = errors = 0
    for kind, entries in _JOBS:
        for entry in entries:
            label = _entry_label(kind, entry)
            fd = finalize_entry(
                entry, kind=kind
            )  # 型検査済みだが正規化済み entry/整形テキストを得る
            normalized = fd.entry.model_dump(mode="json")
            res = record_store.save_document(
                kind,
                normalized,
                rendered_text=fd.formatted or "",
                author_kind="ai",
                actor=args.actor,
                now=now,
            )
            if res.get("status") != "saved":
                errors += 1
                print(f"  [save-error] {label}: {res}")
                continue
            saved += 1
            if args.approve:
                ares = record_store.approve_document(kind, normalized, actor=args.actor, now=now)
                if ares.get("status") == "approved":
                    approved += 1
                else:
                    print(f"  [approve-error] {label}: {ares}")
            print(f"  [saved] {label}（seq {res.get('version_seq')}）")

    print(f"\n完了: 保存 {saved} / 承認 {approved} / 失敗 {errors}（計 {total}）")
    if errors:
        raise SystemExit(1)


if __name__ == "__main__":
    main()
