def diary_days() -> list[date]:
    """日誌を書く平日（土日祝除く）の一覧＝決定的。"""
    days = []
    d = DIARY_START
    while d <= DIARY_END:
        if d.weekday() < 5 and d not in HOLIDAYS:
            days.append(d)
        d += timedelta(days=1)
    return days


