def _att(present: list[str], absent: list[tuple[str, str]] | None = None) -> list[dict]:
    """出欠（クラス日誌）。present=出席の仮名、absent=(仮名, 欠席理由)。"""
    rows = [{"child_id": c, "present": True, "reason": None} for c in present]
    rows += [{"child_id": c, "present": False, "reason": r} for c, r in (absent or [])]
    return rows


