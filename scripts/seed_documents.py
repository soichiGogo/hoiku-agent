"""代表児の書類チェーン（日誌→月案→保育経過記録→保育要録）を書類アーカイブへ投入する seed。

設計コンテキスト §19（集積階層 日誌→月案(L2)→保育経過記録(L3)→要録(L4)）／§5（決定的実体は harness に1つ）。
本番運用ブラッシュアップ：`adk web`/配布UI や scripts の L2/L3/L4 seed（前月・期間の日誌／最終年度の
保育経過記録）は書類アーカイブ（record_store＝Cloud SQL）から自動取得する（未接続はサンプル降格）。その
**参照元となる実データ相当の蓄積**を、実在しない仮名の代表児について厚く用意し、精度・実運用に即した
参照（月案の前月集積・保育経過記録の期間集積・要録の年集積・「書類を見る」タブ）を効かせるための seed。

作り方（§6 の実 LLM パイプラインと同等品質を、決定的・課金ゼロで）:
- 各書類は本ファイルに手書きした entry dict（スキーマ準拠・様式テンプレート準拠）で持つ。
- harness の `finalize_entry(kind=...)` を通して **確定処理（表記正規化→必須欄/年齢分岐の検査→標準様式整形）**
  を実行し、型成立（`.ok`）を確認してから `record_store.save_document` で版管理つきに永続化する
  （＝配布UIの確定と同じ確定ロジックを通る・二重実装しない）。パイプライン（LLM）は回さない。
- 集積チェーンが実際に seed 解決されるよう、日誌は各期の日付に配置し（`list_diary_entries` が拾う＝L2/L3）、
  保育経過記録は児童別・期順に置く（`list_child_record_entries` が拾う＝L4）。

**実名は扱わない（§14）**：登場する子どもは `scripts/seed_children.py` と同じ実在しない仮名ロスターのみ。

接続先は `DATABASE_URL`（config＝env が唯一の出所）。未設定/未マイグレーションは降格（何もせず終了）。本番
Cloud SQL へ入れるときは Auth Proxy 経由の TCP URL を渡す（手順は docs/ライブ実行手順.md）:
    DATABASE_URL='postgresql+psycopg://USER:PASS@127.0.0.1:5432/hoiku' \
        uv run python scripts/seed_documents.py
型の妥当性だけ確認する（DB へ書かない・creds 不要）:
    uv run python scripts/seed_documents.py --dry-run
"""

