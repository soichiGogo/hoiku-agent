# ── 日誌の合成（平日毎日・週テーマ×観察プールを日付インデックスで決定的にローテ） ──


def _weather_temp(d: date, weather_pool: list[str]) -> tuple[str, str]:
    """日付から天気（プールを曜日でローテ）と季節らしい気温を決定的に返す。"""
    weather = weather_pool[d.weekday() % len(weather_pool)]
    base = {4: 18, 5: 22, 6: 24, 7: 29}[d.month]
    temp = base + (d.day % 5) - 2  # ±で日毎に散らす（決定的）
    return weather, f"{temp}℃"


def _season_for(d: date, seasons: list) -> tuple:
    """月内の前半/後半で週テーマ（"MM"/"MMb"）を選ぶ。"""
    table = {s[0].replace("2026-", f"{d.year}-"): s for s in seasons}
    # 4〜7月は "MM"/"MMb" の2枠。15日以前は前半、以降は後半。
    prefix = f"{d.year}-{d.month:02d}"
    first = table.get(prefix)
    second = table.get(prefix + "b")
    if second is None:
        return first
    return first if d.day <= 15 else second


def _rotate(pool: list, idx: int):
    """プールを日付インデックスで決定的に選ぶ（乱数なし）。"""
    return pool[idx % len(pool)]


def _absent_for(d: date, roster: list[str]) -> list[tuple[str, str]]:
    """ときどき欠席を作る（決定的＝週の特定曜日に1人・理由つき）。"""
    # 隔週の火曜に1人欠席（体調）＝現場らしさ。日付で決定的に対象を選ぶ。
    if d.weekday() == 1 and (d.isocalendar().week % 2 == 0):
        who = roster[(d.day) % len(roster)]
        return [(who, "発熱のため欠席")]
    return []


